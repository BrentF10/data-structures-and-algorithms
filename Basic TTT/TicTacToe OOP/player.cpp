#include "Player.h"

using namespace std;

// ----------------------------------------------------------------------------------
// Default constructor: initializes names to empty, piece to space, and stats to 0.
// ----------------------------------------------------------------------------------
Player::Player()
{
    firstName = "";
    lastName = "";
    piece = ' ';
    wins = 0;
    losses = 0;
    draws = 0;
}

// ----------------------------------------------------------------------------------
// Overloaded constructor: initializes first/last name and assigns the player's piece.
// ----------------------------------------------------------------------------------
Player::Player(string first, string last, char p)
{
    firstName = first;
    lastName = last;
    piece = p;
}

// ----------------------------------------------------------------------------------
// Returns the player's first name.
// ----------------------------------------------------------------------------------
string Player::getFirstName()
{
    return firstName;
}

// ----------------------------------------------------------------------------------
// Returns the player's last name.
// ----------------------------------------------------------------------------------
string Player::getlastName()
{
    return lastName;
}

// ----------------------------------------------------------------------------------
// Returns the player's full name in "First Last" format.
// ----------------------------------------------------------------------------------
string Player::getFullName()
{
    return firstName + " " + lastName;
}

// ----------------------------------------------------------------------------------
// Returns the player's piece character.
// ----------------------------------------------------------------------------------
char Player::getPiece()
{
    return piece;
}

// ----------------------------------------------------------------------------------
// Returns the player's number of wins.
// ----------------------------------------------------------------------------------
int Player::getWins()
{
    return wins;
}

// ----------------------------------------------------------------------------------
// Returns the player's number of losses.
// ----------------------------------------------------------------------------------
int Player::getLoses()
{
    return losses;
}

// ----------------------------------------------------------------------------------
// Returns the player's number of draws.
// ----------------------------------------------------------------------------------
int Player::getDraws()
{
    return draws;
}

// ----------------------------------------------------------------------------------
// Sets the player's piece character.
// ----------------------------------------------------------------------------------
void Player::setPiece(char p)
{
    piece = p;
}

// ----------------------------------------------------------------------------------
// Sets the player's first and last names.
// ----------------------------------------------------------------------------------
void Player::setNames(string first, string last)
{
    firstName = first;
    lastName = last;
}

// ----------------------------------------------------------------------------------
// Increments the player's win count by 1.
// ----------------------------------------------------------------------------------
void Player::incrementWins()
{
    wins = wins + 1;
}

// ----------------------------------------------------------------------------------
// Increments the player's loss count by 1.
// ----------------------------------------------------------------------------------
void Player::incrementLosses()
{
    losses = losses + 1;
}

// ----------------------------------------------------------------------------------
// Increments the player's draw count by 1.
// ----------------------------------------------------------------------------------
void Player::incrementDraws()
{
    draws = draws + 1;
}

// ----------------------------------------------------------------------------------
// Returns the length of the player's first name.
// ----------------------------------------------------------------------------------
int Player::getFirstNameLength()
{
    return firstName.size();
}